-- ==========================================================================
--  DemoRuleEngine — Database Migration Script
--  Adds audit columns to Workflows, WorkflowRules, and RuleAuditLogs tables.
--
--  Run this script ONCE against the target SQL Server database.
--  All statements are idempotent (safe to re-run).
--
--  Columns added:
--    Workflows           : CreatedBy(INT), CreatedAt, ModifiedBy(INT), ModifiedAt
--    WorkflowRules       : CreatedBy(INT), CreatedAt, ModifiedBy(INT), ModifiedAt
--    RuleAuditLogs       : RuleId(INT), ChangedById(INT)
--    RuleAuditLogs       : drops legacy NVARCHAR ChangedBy column
-- ==========================================================================

SET NOCOUNT ON;
GO

-- ??????????????????????????????????????????????????????????????????????????
--  1)  Workflows  ?  add audit columns
-- ??????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'CreatedBy' AND Object_ID = OBJECT_ID(N'dbo.Workflows'))
BEGIN
    ALTER TABLE dbo.Workflows ADD CreatedBy INT NULL;
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'CreatedAt' AND Object_ID = OBJECT_ID(N'dbo.Workflows'))
BEGIN
    ALTER TABLE dbo.Workflows
        ADD CreatedAt DATETIME2 NOT NULL
        CONSTRAINT DF_Workflows_CreatedAt DEFAULT (SYSUTCDATETIME());
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'ModifiedBy' AND Object_ID = OBJECT_ID(N'dbo.Workflows'))
BEGIN
    ALTER TABLE dbo.Workflows ADD ModifiedBy INT NULL;
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'ModifiedAt' AND Object_ID = OBJECT_ID(N'dbo.Workflows'))
BEGIN
    ALTER TABLE dbo.Workflows ADD ModifiedAt DATETIME2 NULL;
END
GO

-- ??????????????????????????????????????????????????????????????????????????
--  2)  WorkflowRules  ?  add audit columns
-- ??????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'CreatedBy' AND Object_ID = OBJECT_ID(N'dbo.WorkflowRules'))
BEGIN
    ALTER TABLE dbo.WorkflowRules ADD CreatedBy INT NULL;
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'CreatedAt' AND Object_ID = OBJECT_ID(N'dbo.WorkflowRules'))
BEGIN
    ALTER TABLE dbo.WorkflowRules
        ADD CreatedAt DATETIME2 NOT NULL
        CONSTRAINT DF_WorkflowRules_CreatedAt DEFAULT (SYSUTCDATETIME());
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'ModifiedBy' AND Object_ID = OBJECT_ID(N'dbo.WorkflowRules'))
BEGIN
    ALTER TABLE dbo.WorkflowRules ADD ModifiedBy INT NULL;
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'ModifiedAt' AND Object_ID = OBJECT_ID(N'dbo.WorkflowRules'))
BEGIN
    ALTER TABLE dbo.WorkflowRules ADD ModifiedAt DATETIME2 NULL;
END
GO

-- ??????????????????????????????????????????????????????????????????????????
--  3)  RuleAuditLogs  ?  add RuleId, ChangedById; drop legacy ChangedBy
-- ??????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'RuleId' AND Object_ID = OBJECT_ID(N'dbo.RuleAuditLogs'))
BEGIN
    ALTER TABLE dbo.RuleAuditLogs ADD RuleId INT NULL;
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'ChangedById' AND Object_ID = OBJECT_ID(N'dbo.RuleAuditLogs'))
BEGIN
    ALTER TABLE dbo.RuleAuditLogs ADD ChangedById INT NULL;
END
GO

-- Drop legacy NVARCHAR ChangedBy column (was replaced by ChangedById INT)
IF EXISTS (
    SELECT 1 FROM sys.columns
    WHERE Name = N'ChangedBy' AND Object_ID = OBJECT_ID(N'dbo.RuleAuditLogs'))
BEGIN
    -- Drop default constraint first (if any)
    DECLARE @dfName SYSNAME;
    SELECT @dfName = dc.name
    FROM sys.default_constraints dc
    INNER JOIN sys.columns c
        ON dc.parent_object_id = c.object_id
       AND dc.parent_column_id  = c.column_id
    WHERE dc.parent_object_id = OBJECT_ID(N'dbo.RuleAuditLogs')
      AND c.name = N'ChangedBy';

    IF @dfName IS NOT NULL
        EXEC('ALTER TABLE dbo.RuleAuditLogs DROP CONSTRAINT [' + @dfName + ']');

    ALTER TABLE dbo.RuleAuditLogs DROP COLUMN ChangedBy;
END
GO

-- ??????????????????????????????????????????????????????????????????????????
--  4)  Indexes for fast audit lookups
-- ??????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.indexes
    WHERE name = N'IX_RuleAuditLogs_RuleId'
      AND object_id = OBJECT_ID(N'dbo.RuleAuditLogs'))
BEGIN
    CREATE INDEX IX_RuleAuditLogs_RuleId
        ON dbo.RuleAuditLogs (RuleId);
END
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.indexes
    WHERE name = N'IX_RuleAuditLogs_ChangedById'
      AND object_id = OBJECT_ID(N'dbo.RuleAuditLogs'))
BEGIN
    CREATE INDEX IX_RuleAuditLogs_ChangedById
        ON dbo.RuleAuditLogs (ChangedById);
END
GO

PRINT '=== Migration completed successfully ===';
GO
